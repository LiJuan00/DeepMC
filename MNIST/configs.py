# 命令行输出带颜色的文本
class bcolors:
    # 紫色
    HEADER = '\033[95m'
    # 蓝色
    OKBLUE = '\033[94m'
    # 绿色
    OKGREEN = '\033[92m'
    # 黄色
    WARNING = '\033[93m'
    # 深红色
    FAIL = '\033[91m'
    # 关闭所有属性
    ENDC = '\033[0m'
    # 设置高亮度
    BOLD = '\033[1m'
    # 设置下划线
    UNDERLINE = '\033[4m'
